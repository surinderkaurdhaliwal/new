If my neighbouring farmers haven’t started to harvest their wheat, they are about to. The gravel roads are a joyous frenzy of grain trucks, combines and other farm equipment. Farmers love this time of year, especially when they are pulling in a high-yielding crop that is priced well in the market.

As for us, we wait. Our farm did not grow wheat, canola or any other crop that is harvested at this time of year. It feels strange not being able to share in the excitement of harvest. When a farm kicks off harvest, it sets in motion a bout of activity that usually lasts well into fall and sometimes shares some calendar days with winter.

