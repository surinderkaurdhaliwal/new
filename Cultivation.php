<p style="font-size:23px;font-style:italic;color:#FFF;">

The term “cost of Cultivation” and “Cost of production” is used as synonyms for the purpose of cost study. However, nice distinction can be made between the two, the cost of cultivation includes factor costs up to the stage of gathering the harvest and that cost of production to include factor costs up to the stage of marketing the produce.<br>

Per unit Cost of Production: Cost of production is to be worked out as cost per unit or area and production i.e. per hectare and per quintal/tonnes.<br>

A) Per hectare cost of production: Total cost / Area under crop (ha)<br>
B) Per quintal/tonne cost of production: Total cost – Value of by product/ Qty of main produce in quintals/tonnes.<br>

Measures of Farm Income: The profits at different cost levels provide different measure of returns to the cultivator. These are discussed below<br>

1) Profit at Cost A: It is also known as farm business income. It provides an estimate of returns to the farmer for his investment and profit.
Farm Business Income = Gross Returns – Cost A<br>

2) Profit at Cost B: It is also termed as family labour income. It provides an estimate of returns to the farmer for his labour and profit.
Family labour Income= Gross returns - Cost B<br>

3) Profit as Cost C: It is also known as net income. It provides an estimate of returns to the farmer purely of profit.
Net Income = Gross returns - Cost C.<br>
Gross returns or gross income is the total of the values of both the main and byproducts.
Farm business income, family labour income, and net income are the measures of farm income.

